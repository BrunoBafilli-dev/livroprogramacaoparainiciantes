/*
3 - Crie um programa que utilize um loop 'for' para exibir os números de 1 a 5, um em cada linha.
*/

/**************************Solução abaixo*********************************/
for (int i = 1; i <= 5; i++)  // Inicia um loop 'for' que começa em 1, executa enquanto 'i' for menor ou igual a 5, e incrementa 'i' a cada iteração.
{
    Console.WriteLine(i);  // Exibe o valor de 'i' na tela.
}