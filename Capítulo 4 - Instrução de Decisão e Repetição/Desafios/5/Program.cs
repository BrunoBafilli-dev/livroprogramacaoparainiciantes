Console.WriteLine("Contagem regressiva de 10 a 1:"); // Exibe a mensagem para o usuário no console, informando a contagem regressiva.

int contador = 10; // Inicializa a variável 'contador' com o valor 10 para iniciar a contagem regressiva.

// Loop while para exibir a contagem regressiva.
// O loop continuará enquanto a variável 'contador' for maior ou igual a 1.
while (contador >= 1)
{
    Console.WriteLine(contador); // Exibe o valor atual do contador no console.

    contador--; // Decrementa o contador em 1 a cada iteração do loop para fazer a contagem regressiva.
}