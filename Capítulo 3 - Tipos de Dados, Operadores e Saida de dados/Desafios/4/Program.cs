// Números inteiros A e B
int A = 23;
int B = 15;

// A. Soma: A + B
int soma = A + B;
Console.WriteLine("A. Soma: " + soma);

// B. Subtração: A - B
int subtracao = A - B;
Console.WriteLine("B. Subtração: " + subtracao);

// C. Divisão: A / B
int divisao = A / B;
Console.WriteLine("C. Divisão: " + divisao);

// D. Multiplicação: A * B
int multiplicacao = A * B;
Console.WriteLine("D. Multiplicação: " + multiplicacao);